# Obtener las dimensiones de la imagen
# h: altura, w: ancho, c: canales (profundidad)
h, w, c = img.shape

# Crear una matriz de h filas y w columnas de ceros
pocoyo_gris1 = np.zeros((h,w))

sum_valor_canales = 0

# Para cada fila de la imagen
for i in range(h):
  # Para cada columna de la imagen
  for j in range(w):
    # Para cada canal de la imagen
    for k in range(c):
      # Sumar el dato de los valores de cada canal 
      # para cada pixel ubicado en i, j 
      sum_valor_canales += img[i][j][k]
    # Calcular el promedio de cada pixel
    sum_valor_canales = sum_valor_canales / 3

    pocoyo_gris1[i][j] =  sum_valor_canales
    sum_valor_canales = 0

pocoyo_gris1.shape

plt.figure()
plt.imshow(pocoyo_gris1, cmap="gray")

